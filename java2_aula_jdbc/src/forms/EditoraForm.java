    private void bnt_sv_edActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnt_sv_edActionPerformed
        Editora editora = new Editora();
        editora.setNome_editora(txt_nome_ed.getText());
        editora.setMun_editora(txt_mun_ed.getText());

        try {
            if (mode.equals("INS")) {
                editoraDAO.save(editora);
            } else if (mode.equals("UPD")) {
                editoraDAO.update(editora);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        listar();

    }//GEN-LAST:event_bnt_sv_edActionPerformed

