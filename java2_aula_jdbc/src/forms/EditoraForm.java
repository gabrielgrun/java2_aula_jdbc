    private void btn_rmv_edActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_rmv_edActionPerformed
        Editora editora = new Editora();

        editora.setEditora_id(Integer.parseInt(txt_id_ed.getText()));
        try { 
        editoraDAO.delete(editora);
        txt_id_ed.setText("");
        txt_nome_ed.setText("");
        txt_mun_ed.setText("");
        this.mode = "INS";
        listar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btn_rmv_edActionPerformed
    private void listar() {
        DefaultTableModel modelo = (DefaultTableModel)tabela_ed.getModel();
        modelo.setNumRows(0);
        
        try {
            for (Editora editora : editoraDAO.findAll()) {
                String linha [] = {""+editora.getEditora_id(), 
                editora.getNome_editora(), editora.getMun_editora()};
                
                modelo.addRow(linha);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
