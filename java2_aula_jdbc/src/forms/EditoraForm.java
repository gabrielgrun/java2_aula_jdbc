    private void tabela_edMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela_edMouseClicked
        int selected = tabela_ed.getSelectedRow();
        txt_id_ed.setText(tabela_ed.getValueAt(selected, 0).toString());
        txt_nome_ed.setText(tabela_ed.getValueAt(selected, 1).toString());
        txt_mun_ed.setText(tabela_ed.getValueAt(selected, 2).toString());
        this.mode = "UPD";
    }//GEN-LAST:event_tabela_edMouseClicked

